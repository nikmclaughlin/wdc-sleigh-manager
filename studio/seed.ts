const client = getCliClient()

async function run() {
  // Get all unique types
  const uniqueTypes = new Set(ALL_TYPE_DOCUMENTS.map((doc) => doc._type))

  // Delete all existing content
  await client.delete({query: '*[_type in $types]', params: {types: Array.from(uniqueTypes)}})

  // Create new docs
  const tx = client.transaction()
  ALL_TYPE_DOCUMENTS.forEach((doc) => {
    tx.createOrReplace(doc)
  })
  tx.commit({
    autoGenerateArrayKeys: true,
  })
    .then((res) => {
      console.log(res)
    })
    .catch((err) => {
      console.error(err)
    })
}

run()
