// remove all non-alphanumeric, multiple dashes, leading/trailing dashes
function slugify(str: string) {
  return str
    .toLowerCase()
    .replace(/[^a-z0-9]/gi, '-')
    .replace(/-+/g, '-')
    .replace(/^-|-$/g, '')
}

